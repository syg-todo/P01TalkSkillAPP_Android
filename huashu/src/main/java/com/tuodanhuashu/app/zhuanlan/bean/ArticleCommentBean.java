package com.tuodanhuashu.app.zhuanlan.bean;

public class ArticleCommentBean {
    private String name = "";

    private String content = "";

    private String create_time = "";

    private String heade_img = "";

    public ArticleCommentBean() {
    }

    public String getname() {
        return name;
    }

    public void setname(String name) {
        this.name = name;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getCreate_time() {
        return create_time;
    }

    public void setCreate_time(String create_time) {
        this.create_time = create_time;
    }

    public String getHeade_img() {
        return heade_img;
    }

    public void setHeade_img(String heade_img) {
        this.heade_img = heade_img;
    }
}
